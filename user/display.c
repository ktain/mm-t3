int32_t displayDelayCounter;
char* displayStr = "";

int32_t displayIndex;

void writeControlRegister(char c)
{
	RS_1;	// Switch to control register
	CE_0;	// Enable data writing

	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
	SPI_I2S_SendData(SPI2, c);
	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_BSY) == SET);

	CE_1;	// Latch on
	RS_0;	// Switch back to data register
}


void display(char* str) {

	if (strcmp(displayStr,str)) {	// change to new display string
		displayStr = str;
		displayIndex = 0;
	}

	char const *ptr;	// to keep track of start of str
	RS_0;			// Select control register
	CE_0;			// Enable data writing


	// Static Display
	int32_t i;
	for (i = 0; displayStr[i] != '\0'; i++);	// count characters
	if (i <= 3) {
		for(int32_t i = 0; i < 4; i++) {
			ptr = &fontTable[displayStr[i]*5];
			for(int32_t j = 0; j < 5; j++) {
				while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
				SPI_I2S_SendData(SPI2, *ptr);
				ptr++;
			}
		}
	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_BSY) == SET);
	CE_1;	//latch on
	}


	// Scrolling display
	if (displayStr[displayIndex+3] == '\0') {
		displayIndex = 0;
		return;
	}

	// Display each of 20 columns
	for(int32_t j = 0; j < 4; j++) {
		ptr = &fontTable[displayStr[displayIndex+j]*5];
		for(int32_t k = 0; k < 5; k++) {
			while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
			SPI_I2S_SendData(SPI2, *ptr);
			ptr++;
		}
	}
	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_TXE) == RESET);
	while (SPI_I2S_GetFlagStatus(SPI2, SPI_I2S_FLAG_BSY) == SET);
	CE_1;	//latch on

	displayIndex++;

}

void displayFloat(float f) {
	char str[5];
	sprintf(str, "%.1f", f);
	display(str);
}

void displayInt(int32_t i) {
	char str[5];
	sprintf(str, "%4d", i);
	display(str);
}


void clearDisplay(void)
{
	displayStr = "";
}

void setDisplayBrightness(int8_t brightness) {
	if (brightness < 0)
		brightness = 0;
	else if (brightness > 15)
		brightness = 15;

	writeControlRegister(0x60 | brightness);
}

